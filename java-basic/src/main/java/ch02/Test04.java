// 정수 값을 메모리에 저장하는 원리

package ch02;

public class Test04 {
  public static void main(String[] args) {
    // 5 - 5 의 결과를 출력하라.
    System.out.println(5 - 5);
    // 5 + (-5)의 결과를 출력하라.
    System.out.println(5 + (-5));
  }
}

